            <div class="col-12  transparency__header-subtitle">
                <p>Las actividades económicas que desarrolla RAYOGAS SA ESP se encuentran definidas en la
                    regulación emitida por la Comisión de Regulación de Energía y Gas.</p>
            </div>
            <div class="col-12 glosary">
                <div class="row">
                    <div class="col-12 col-md-6">
                        <p><strong class="gray">Fuente:</strong> Resoluciones CREG 023, 044, 122, 165 de
                            2008, 001, 092,
                            181, 180 de 2009,
                            004 de 2010, 053, 108, 177 de 2011, 137, 138 de 2013, 153 de 2014, 154 de 2014,
                            095, 149 de 2015, 063 de 2016.</p>
                        <p><strong class="gray">Capacidad de compra:</strong> Para efectos de esta
                            resolución debe
                            interpretarse de
                            acuerdo a lo dispuesto en la Resolución número CREG 063 de 2016 o aquellas que
                            la modifiquen o sustituyan.</p>
                        <p><strong class="gray">Capacidad disponible de compra:</strong> Corresponde a la
                            cantidad total
                            de GLP, medida en
                            kilogramos, que puede comprar un distribuidor en el mercado mayorista, durante
                            lo que resta del periodo de compra. Esta capacidad será considerada, calculada y
                            publicada para cada distribuidor, de acuerdo con lo establecido en la
                            regulación.</p>
                        <p><strong class="gray">Cargo de Comercialización Minorista de GLP:</strong> Es el
                            cargo unitario
                            en pesos por
                            kilogramo ($/kilogramo) que remunera la actividad de comercialización minorista
                            de GLP.</p>
                        <p><strong class="gray">Cargo de Distribución de GLP:</strong> Es el cargo unitario
                            en pesos por
                            kilogramo
                            ($/kilogramo) que remunera la actividad de comercialización minorista de GLP.
                        </p>
                        <p><strong class="gray">Ciclo de nominación de suministro:</strong> Proceso
                            estandarizado que se
                            inicia con el
                            anuncio de cantidades disponibles de producto por parte de Ecopetrol y que
                            termina con la confirmación del respectivo programa de entregas.</p>
                        <p><strong class="gray">Cilindro:</strong> Recipiente utilizado en la prestación del
                            servicio
                            público domiciliario
                            de Gas Licuado Petróleo, GLP, con capacidad entre 5 y 46 kilogramos (kg) de GLP
                            que puede ser metálico o de construcción compuesta, y que cumple con lo previsto
                            en el Reglamento Técnico vigente, expedido por el Ministerio de Minas y Energía.
                        </p>
                        <p><strong class="gray">Cilindros Universales Adecuados:</strong> Son los cilindros
                            del parque
                            universal que,
                            únicamente durante el Período de Transición y/o el Periodo de Cierre, fueron
                            encontrados aptos para continuar prestando el servicio y, una vez comprados por
                            un distribuidor, fueron marcados por este como cilindros de su propiedad de
                            conformidad con la regulación respectiva y los reglamentos técnicos del
                            Ministerio de Minas y Energía.</p>
                        <p><strong class="gray">Cilindros Universales Remanentes:</strong> Son los cilindros
                            del parque
                            universal que, una
                            vez terminado el Período de Transición, permanecen en el mercado por no haber
                            sido objeto de destrucción y/o adecuación por parte de un Distribuidor, cuya
                            recolección será regulada por la CREG.</p>
                        <p><strong class="gray">Comercialización Mayorista de GLP:</strong> Actividad
                            consistente en la
                            compra y venta de
                            GLP al por mayor y a granel, con destino al Servicio Público Domiciliario de Gas
                            Combustible.</p>
                        <p><strong class="gray">Comercialización Minorista de GLP:</strong> Actividad que
                            consiste en la
                            entrega de GLP en
                            cilindros en el domicilio del usuario final o en expendios. Incluye la compra
                            del producto envasado mediante contrato exclusivo con un distribuidor, cuando
                            aplique, el flete del producto en cilindros, la celebración de los contratos de
                            servicios públicos con los usuarios y la atención comercial de los usuarios.</p>
                        <p><strong class="gray">Comercializador Mayorista de GLP:</strong> Empresa de
                            servicios públicos,
                            salvo lo
                            dispuesto en el artículo 15.2 de la Ley 142 de 1994, cuya actividad es la
                            comercialización de GLP, producido y/o importado directamente o por terceros, a
                            distribuidores de GLP, otros comercializadores mayoristas y usuarios no
                            regulados.</p>
                        <p><strong class="gray">Comercializador Minorista de GLP:</strong> Empresa de
                            servicios públicos,
                            que cumpliendo
                            con los requisitos exigidos en la resolución CREG 023 de 2008, ejerce la
                            actividad de Comercialización Minorista. El Comercializador Minorista de GLP
                            puede ser a la vez Distribuidor de GLP.</p>
                        <p><strong class="gray">Comisión o CREG:</strong> Comisión de Regulación de Energía
                            y Gas,
                            organizada como Unidad
                            Administrativa Especial del Ministerio de Minas y Energía de acuerdo con las
                            Leyes 142 y 143 de 1994.</p>
                        <p><strong class="gray">Contrato de Servicios Públicos Especial:</strong> Es un
                            Contrato de
                            Servicios Públicos,
                            donde algunas de las estipulaciones son objeto de acuerdo especial con uno o
                            algunos usuarios.</p>
                        <p><strong class="gray">Contrato de Servicios Públicos:</strong> Es un contrato
                            uniforme,
                            consensual, en virtud
                            del cual una empresa de servicios públicos los presta a un usuario a cambio de
                            un precio en dinero, de acuerdo con lo establecido en la ley y la regulación.
                        </p>
                        <p><strong class="gray">Contrato de suministro de GLP envasado:</strong> Acuerdo de
                            voluntades
                            mediante el cual un
                            Comercializador Minorista se compromete con un Distribuidor a comercializar
                            exclusivamente cilindros de ese Distribuidor. En este contrato se pactan, además
                            de la exclusividad, las condiciones técnicas, administrativas y comerciales del
                            suministro de GLP envasado.</p>
                        <p><strong class="gray">Contrato de suministro:</strong> Es el contrato escrito
                            celebrado entre
                            un Comercializador
                            Mayorista y un Distribuidor o un Usuario No Regulado, para la venta por parte
                            del primero y la compra por parte del segundo, de GLP al por mayor y a granel.
                            También se denomina Contrato de Suministro al celebrado entre el Comercializador
                            Mayorista que vende GLP con Precio Regulado y otros Comercializadores
                            Mayoristas.</p>
                        <p><strong class="gray">Contrato firme:</strong> Contratos en los que el
                            Comercializador
                            Mayorista se compromete a
                            entregar un volumen diario garantizado de GLP durante un período previamente
                            pactado en el contrato.</p>
                        <p><strong class="gray">Depósito de cilindros de GLP:</strong> Centro de acopio,
                            destinado al
                            almacenamiento de
                            cilindros de GLP como mecanismo operativo de la actividad de Comercialización
                            Minorista a usuarios finales. Sus características técnicas deben corresponder a
                            las establecidas en el Reglamento Técnico vigente expedido por el Ministerio de
                            Minas y Energía y debe contar con la aprobación vigente de las autoridades
                            competentes.</p>
                        <p><strong class="gray">Depósito de Garantía:</strong> Monto de dinero que el
                            usuario debe
                            entregar al
                            Comercializador Minorista para garantizar el buen uso y la conservación del
                            cilindro de propiedad del distribuidor, durante el período de tenencia del
                            cilindro por el usuario.</p>
                        <p><strong class="gray">Distribución de GLP:</strong> Actividad que comprende: i) la
                            compra del
                            GLP en el mercado
                            mayorista con destino al usuario final, ii) el flete desde el Punto de Entrega
                            del comercializador mayorista, o desde el Punto de Entrega del Transportador,
                            hasta las plantas de envasado, iii) el envasado de cilindros marcados y iv) la
                            operación de la planta de envasado correspondiente. Comprende además las
                            actividades de flete y entrega de producto a granel a través de tanques
                            estacionarios instalados en el domicilio de los usuarios finales y de venta de
                            cilindros a través de Puntos de Venta.</p>
                        <p><strong class="gray">Envasado de cilindros de GLP:</strong> Actividad que
                            consiste en llenar
                            un cilindro
                            portátil con GLP en una Planta Envasadora y la operación de esta última. Esta
                            actividad incluye la revisión y clasificación de los cilindros para su
                            mantenimiento o destrucción con sujeción al Reglamento Técnico vigente expedido
                            por Ministerio de Minas y Energía, y el drenaje, previo al llenado, de los
                            residuos no volátiles que permanecen en los mismos después de que el usuario ha
                            utilizado el combustible envasado.</p>
                        <p><strong class="gray">Envases de GLP:</strong> Son los recipientes que contienen
                            GLP para uso
                            combustible en el
                            domicilio de un usuario final pero que, por su tamaño, menor a 5 kg de capacidad
                            de GLP, o por sus características y accesorios especiales que le permiten ser
                            usados tomando el GLP en su fase líquida y no como comúnmente se hace en forma
                            gaseosa, no hacen parte del grupo de cilindros a los que se refiere el
                            Reglamento Técnico del Ministerio de Minas y Energía Resolución 18 0196 del 21
                            de febrero de 2006, así como de aquellos actos administrativos que lo
                            modifiquen, sustituyan o adicionen.</p>
                        <p><strong class="gray">Eventos de fuerza mayor, caso fortuito o causa
                                extraña:</strong> Eventos
                            que de acuerdo
                            con los artículos 64 del Código Civil y 992 del Código de Comercio, o aquellos
                            que los modifiquen o sustituyan, eximen de la responsabilidad por incumplimiento
                            parcial o total de obligaciones contractuales, si el mismo se deriva de ellos.
                            Dichos eventos deben ser imprevisibles, irresistibles y sin culpa de quien
                            invoca la causa eximente de responsabilidad.</p>
                        <p><strong class="gray">Expendio de Cilindros de GLP:</strong> Instalación dedicada
                            exclusivamente a la venta de
                            cilindros de GLP a usuarios finales para garantizar la continuidad en la
                            prestación del Servicio Público Domiciliario de GLP. Sus características
                            técnicas deben corresponder a las establecidas en el Reglamento Técnico vigente
                            expedido por el Ministerio de Minas y Energía y debe contar para su operación
                            con la aprobación vigente de las autoridades competentes.</p>
                        <p><strong class="gray">Flete:</strong> Actividad que consiste en el traslado de GLP
                            a granel
                            entre puntos de
                            entrega del producto o los puntos de salida del sistema de transporte y las
                            plantas envasadoras, y/o el traslado de GLP en cilindros o cisternas entre la
                            planta envasadora y los municipios atendidos.</p>
                        <p><strong class="gray">Fórmula Tarifaria Específica:</strong> Conjunto de criterios
                            y de métodos
                            de carácter
                            particular, sujetos a las Fórmulas Tarifarias Generales, resumidos por medio de
                            una fórmula, en virtud de los cuales cada comercializador puede modificar
                            periódicamente las tarifas que cobra a sus usuarios regulados. Cuando se haga
                            referencia a fórmula tarifaria de una empresa debe entenderse la Fórmula
                            Tarifaria Específica.</p>
                    </div>
                    <div class="col-12 col-md-6">
                        <p> <strong class="gray">Fórmulas Tarifarias Generales:</strong> Conjunto de
                            criterios y de métodos de tipo general en virtud de los cuales se determina a
                            los comercializadores de gas que atienden a usuarios regulados, la tarifa
                            promedio por unidad de Gas Combustible.</p>
                        <p> <strong class="gray">Gas Combustible:</strong> Es cualquier gas que pertenezca a
                            una de las tres familias de gases combustibles (gases manufacturados, Gas
                            Natural y Gas Licuado de Petróleo) y cuyas características permiten su empleo en
                            artefactos a gas, según lo establecido en la Norma Técnica Colombiana NTC-3527,
                            o aquellas que la aclaren, modifiquen o sustituyan.</p>
                        <p> <strong class="gray">Gas Licuado del Petróleo (GLP):</strong> Es una mezcla de
                            hidrocarburos extraídos del procesamiento del gas natural o del petróleo
                            gaseosos en condiciones atmosféricas, que se licuan fácilmente por enfriamiento
                            o compresión, constituida principalmente por propano y butanos. Su calidad
                            corresponde con las especificaciones y estándares adoptados por la CREG mediante
                            la resolución que establezca la remuneración del producto a los
                            comercializadores mayoristas de GLP.</p>
                        <p> <strong class="gray">Indice de Precios al Productor (IPP):</strong> Indice de
                            Precios al Productor total nacional reportado por la autoridad competente.<br>
                            Instalación para cilindros:</strong> Comprende el regulador y la tubería
                            flexible no metálica, mangueras y/o conectores flexibles, usados en
                            instalaciones de artefactos a gas que utilicen GLP.</p>
                        <p> <strong class="gray">Instalación para cilindros:</strong> Comprende el regulador
                            y la tubería flexible no metálica, mangueras y/o conectores flexibles, usados en
                            instalaciones de artefactos a gas que utilicen GLP.</p>
                        <p> <strong class="gray">Iso-tanques o Iso- contenedores:</strong> Recipientes
                            utilizados para transportar líquidos a granel y que tienen las mismas medidas
                            externas e infraestructura, para su manipulación y movimiento, que un contenedor
                            estándar.</p>
                        <p> <strong class="gray">Libertad Regulada:</strong> Régimen de tarifas mediante el
                            cual la Comisión de Regulación respectiva fijará los criterios y la metodología
                            con arreglo a los cuales las Empresas de Servicios Públicos Domiciliarios pueden
                            determinar o modificar los precios máximos para los servicios ofrecidos al
                            usuario o consumidor.</p>
                        <p> <strong class="gray">Libertad Vigilada:</strong> Régimen de tarifas mediante el
                            cual las Empresas de Servicios Públicos Domiciliarios pueden determinar
                            libremente las tarifas de venta a medianos y pequeños consumidores, con la
                            obligación de informar por escrito a las Comisiones de Regulación sobre las
                            decisiones tomadas sobre esta materia.</p>
                        <p> <strong class="gray">Marca:</strong> Conjunto de caracteres alfanuméricos
                            inscritos en forma indeleble sobre el Cilindro, que cumple los requisitos
                            técnicos que para ese efecto establezca el Ministerio de Minas y Energía, y que
                            hacen posible la identificación del Distribuidor propietario del cilindro y
                            responsable por la seguridad del mismo en los términos definidos en la
                            Resolución CREG 023 de 2008.</p>
                        <p> <strong class="gray">Mercado mayorista de GLP:</strong> Es el mercado en el cual
                            los Comercializadores Mayoristas venden GLP a otros Comercializadores
                            Mayoristas, a Distribuidores o a Usuarios No Regulados, con destino al servicio
                            público domiciliario de GLP.</p>
                        <p> <strong class="gray">Oferta de compra ajustada:</strong> Corresponde a la
                            solicitud de compra, en términos de la Resolución 053 de 2011, de cada
                            distribuidor ajustada de acuerdo con lo establecido en el artículo 13 de la
                            presente resolución.</p>
                        <p> <strong class="gray">Periodo de Cierre:</strong> Periodo de tiempo comprendido
                            entre el 1o de enero y el 30 de junio de 2012, o mientras se cuenten con
                            recursos disponibles del Margen de Seguridad, lo primero que ocurra, durante el
                            cual, se recogen los Cilindros Universales Remanentes, y se ejecutan los
                            trabajos de mantenimiento y reposición requeridos sobre los Tanques
                            Estacionarios, acorde con las reglas que para el efecto establecerá la CREG.</p>
                        <p> <strong class="gray">Periodo de Compra:</strong> Periodo de 6 meses que inicia
                            un primero (1o) de julio de cada año y terminará el treinta y uno (31) de
                            diciembre del mismo año o aquel que inicia del primero (1o) de enero de cada año
                            y termina el treinta (30) de junio del mismo año.</p>
                        <p> <strong class="gray">Periodo de transición:</strong> Periodo de tiempo en el
                            cual se realizó el cambio completo entre un parque de cilindros universal de
                            propiedad de los usuarios y un parque de cilindros marcados de propiedad de los
                            Distribuidores de acuerdo con las normas especiales establecidas en las
                            Resoluciones CREG 045 de 2008 y CREG 147 de 2010, el cual finaliza el 31 de
                            diciembre de 2011.</p>
                        <p> <strong class="gray">Período Tarifario:</strong> Período por el cual la Fórmula
                            Tarifaria General tiene vigencia, de acuerdo con lo establecido en el artículo
                            126 de la Ley 142 de 1994.</p>
                        <p> <strong class="gray">Planta de Envasado de GLP:</strong> Infraestructura física,
                            comprendida en un solo predio, de la cual dispone un Distribuidor para envasar
                            GLP en cilindros de su propiedad, o universales durante el período de
                            transición, y/o para cargar cisternas destinadas a servir tanques estacionarios
                            ubicados en los domicilios de usuarios finales. Sus características técnicas
                            deben corresponder a las establecidas en el Reglamento Técnico vigente expedido
                            por el Ministerio de Minas y Energía y debe contar con la aprobación vigente de
                            las autoridades competentes.</p>
                        <p> <strong class="gray">Precio Libre de GLP:</strong> Precio del GLP cuyo monto
                            puede ser definido libremente entre compradores y vendedores.</p>
                        <p> <strong class="gray">Precio Máximo Regulado de GLP:</strong> Es el precio máximo
                            que por todo concepto paga el distribuidor por el GLP entregado por el
                            comercializador mayorista, en el punto de entrada al sistema de transporte o en
                            las instalaciones que para entrega directa adecue el segundo, en las condiciones
                            y cantidades pactadas en el contrato firme celebrado entre ellos. Este precio es
                            establecido por la CREG para cada punto de suministro indicado en esta
                            resolución.</p>
                        <p> <strong class="gray">Precio Regulado de GLP:</strong> Precio Máximo del GLP que
                            pueden aplicar los comercializadores mayoristas para su venta a Distribuidores y
                            cuyo monto es definido por la CREG o por la aplicación de las fórmulas
                            tarifarias establecidas por la CREG.</p>
                        <p> <strong class="gray">Punto de Entrega del Comercializador Mayorista:</strong>
                            Instalaciones de manejo y entrega de GLP con las que cuenta el Comercializador
                            Mayorista para suministrar el producto a sus compradores, las cuales pueden
                            estar ubicadas, entre otros, en un Punto de Producción, en un Punto de
                            Importación, en un Punto de Recibo del Transportador o en un Punto de Entrega
                            del Transportador.</p>
                        <p> <strong class="gray">Punto de entrega:</strong> Punto físico localizado en la
                            Isla de San Andrés en el cual se realiza la transferencia de custodia del
                            producto del Transportador al Remitente.</p>
                        <p> <strong class="gray">Punto de Importación:</strong> Punto de Entrega de un
                            Comercializador Mayorista, para GLP importado, vinculado a una instalación para
                            la importación de GLP.</p>
                        <p> <strong class="gray">Punto de Producción:</strong> Punto de Entrega de un
                            Comercializador Mayorista, para GLP de producción nacional, el cual está
                            incorporado a las instalaciones de una fuente de producción nacional.</p>
                        <p> <strong class="gray">Punto de recibo:</strong> Es la conexión del punto de
                            importación y/o producción del comercializador mayorista, en donde se hace
                            transferencia de custodia del producto del remitente al transportador.</p>
                        <p> <strong class="gray">Punto de Venta de Cilindros de GLP:</strong> Instalación
                            para la venta de cilindros de GLP a usuarios finales, localizada dentro de los
                            predios de otro establecimiento comercial no dedicado exclusivamente a esa
                            actividad, el cual ha sido autorizado para el efecto por el Ministerio de Minas
                            y Energía y cuenta con la aprobación vigente de las autoridades competentes.</p>
                        <p> <strong class="gray">Red interna de cilindros:</strong> Sistema de tuberías no
                            flexibles, internas o externas a la vivienda que permiten la conducción de gas
                            hacia los distintos artefactos de consumo de un mismo usuario. Está comprendida
                            entre la salida de los centros de medición (o los reguladores de presión para el
                            caso de instalaciones para suministro de gas sin medidor) y los puntos de salida
                            para la conexión de los artefactos de consumo.</p>
                        <p> <strong class="gray">Reglamento de distribución y comercialización minorista de
                                GLP:</strong> Conjunto de normas de carácter general expedidas por la CREG
                            que reglamentan la actividad de las empresas que prestan el Servicio de
                            Distribución y Comercialización Minorista de GLP y su interrelación con los
                            demás Agentes.</p>
                        <p> <strong class="gray">Símbolo Identificador:</strong> Símbolo que acompaña la
                            marca del distribuidor colocada en los cilindros, el cual se constituye, para
                            todos los fines, en la imagen identificadora del nuevo esquema de prestación del
                            servicio de GLP a través de cilindros marcados de propiedad del distribuidor. El
                            símbolo identificador es definido por la CREG.</p>
                        <p> <strong class="gray">Superintendencia de Industria y Comercio, SIC:</strong>
                            Organismo de carácter técnico adscrito al Ministerio de Ambiente, Vivienda y
                            Desarrollo Territorial que goza de autonomía administrativa, financiera y
                            presupuestal de conformidad con el Decreto 2153 de 1992.</p>
                        <p> <strong class="gray">Superintendencia de Servicios Públicos Domiciliarios o
                                SSPD:</strong> Organismo de carácter técnico, adscrito al Departamento
                            Nacional de Planeación, con personería jurídica, autonomía administrativa y
                            patrimonial.</p>
                        <p> <strong class="gray">Tasa de descuento:</strong> Tasa calculada a partir de la
                            estimación del costo promedio ponderado de capital (WACC por sus siglas en
                            inglés) establecida para las actividades de transporte de GLP por ductos y la
                            cadena de distribución de combustibles líquidos.</p>
                        <p> <strong class="gray">Variación:</strong> Valor absoluto de la diferencia entre
                            la cantidad de GLP nominada y aprobada durante el ciclo de nominación, y la
                            cantidad recibida y/o entregada en los puntos de recibo y/o entrega del
                            transportador.</p>
